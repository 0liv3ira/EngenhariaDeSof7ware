package system;

/**
 *
 * @author venom
 */
public class Gestor {
    private String password , login , nome;

    public Gestor(String password, String login, String nome) {
        this.password = password;
        this.login = login;
        this.nome = nome;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    

    
}
